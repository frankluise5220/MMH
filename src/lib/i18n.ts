type I18nKey = keyof typeof I18N_MESSAGES["zh-CN"];

export function translate(language: DisplayLanguage, key: string) {
  const typedKey = key as I18nKey;
  return I18N_MESSAGES[language]?.[typedKey] ?? I18N_MESSAGES["zh-CN"][typedKey] ?? key;
}

export function useI18n() {
  const [language, setLanguage] = useState<DisplayLanguage>("zh-CN");

  useEffect(() => {
    function syncLanguage() {
      setLanguage(getDisplayLanguagePreference());
    }
    syncLanguage();
    window.addEventListener(APP_PREFS_EVENT, syncLanguage);
    return () => window.removeEventListener(APP_PREFS_EVENT, syncLanguage);
  }, []);

  return useMemo(() => ({
    language,
    t: (key: string) => translate(language, key),
  }), [language]);
}
